public class Ex2 {
    public static void main(String[] args) {
        int a=10;
        int b=22;
        System.out.println(a+" + "+b + " = " + (a+b));
        System.out.println(a+" * "+b+"="+(a*b) );
        System.out.println(a+" - "+b+"="+(a-b));
        if (b==0) {
            System.out.println(a+" % "+b+"="+(a%b));
        }else {
            System.out.println(a + " / " + b + "=" + (a / b));
        }
    }
}
